using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Core.DependencyResolvers;
using Core.Extensions;
using Core.Utilities.IoC;
using Core.Utilities.Security.Encryption;
using Core.Utilities.Security.Jwt;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.HttpsPolicy;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Hosting;
using Microsoft.Extensions.Logging;
using Microsoft.IdentityModel.Tokens;

namespace WebAPI
{
    public class Startup
    {
        public Startup(IConfiguration configuration)
        {
            Configuration = configuration;
        }

        public IConfiguration Configuration { get; }

        public void ConfigureServices(IServiceCollection services)
        {
            services.AddControllers();

            // Farklý clientlar üzerinden istek atýldýðýnda güvenlik engelleri ile karþýlaþýlýrz, bunu engellemek için cors ayarlarý yapýlýr.
            services.AddCors(options =>
            {
                options.AddPolicy("AllowOrign", builder =>
                {
                    builder.WithOrigins("http://localhost:3000");
                });
            });

            var tokenOptions = Configuration.GetSection("TokenOptions").Get<TokenOptions>();

            services.AddAuthentication(JwtBearerDefaults.AuthenticationScheme).AddJwtBearer(options =>
            {
                options.TokenValidationParameters = new TokenValidationParameters
                {
                    ValidateIssuer = true,
                    ValidateAudience = true,
                    ValidateLifetime = true,
                    ValidateIssuerSigningKey = true,
                    ValidIssuer = tokenOptions.Issuer,
                    ValidAudience = tokenOptions.Audience,
                    IssuerSigningKey = SecurityKeyHelper.CreateSecurityKey(tokenOptions.SecurityKey)
                };
            });

            // ServiceCollectionExtension'da yazýldý.
            services.AddDependenyResolvers(new ICoreModule[]{
                    new CoreModule()
                });

        }

        public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
        {
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
            }

            app.ConfigureCustomExceptionMiddleware();

            // AllowAnyHeader ile gelen tüm isteklere (get, post, delete, put) izin ver diyoruz.
            app.UseCors(builder => builder.WithOrigins("http://localhost:3000").AllowAnyHeader());

            app.UseHttpsRedirection();

            app.UseRouting();

            // Sisteme giriþ yapmak için gerekli kimlik kontrolünün yapýlmasýný saðlar. Kullanýcý Login iþlemi baþarýlýysa Authenticate olmuþ olur.
            app.UseAuthentication();

            // Sisteme giriþ yaptýktan sonra sayfalarda gezinmek için gerekli yetkilendirmeleri kontrol eder. Kullanýcý Authenticate olduktan sonra Jwt'deki rollerine göre authorize iþlemini gerçekleþtirir.
            app.UseAuthorization();



            app.UseEndpoints(endpoints =>
            {
                endpoints.MapControllers();
            });
        }
    }
}
